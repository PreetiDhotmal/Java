class Operations {

	public static void main(String[] args){

		int a = 20;
		int b = 10;
		int ans = 0;

		ans = a+b;
		System.out.println("Addition = "+ans);

		ans = a-b;
		System.out.println("Substraction = "+ans);

		ans = a*b;
		System.out.println("Multiplication = "+ans);

		ans = a/b;
		System.out.println("Division = "+ans);

		ans = a%b;
		System.out.println("Modulus = "+ans);
	}
}
