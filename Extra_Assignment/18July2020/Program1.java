class OperatorsDemo{

	public static void main(String[] args){

		int x = 20, y = 10;
		boolean ans;

		ans = x < y;
		System.out.println("Ans = "+ ans);	//False

		ans = x > y;
		System.out.println("Ans = "+ ans);	//True

		ans = x <= y;
		System.out.println("Ans = "+ ans);	//False

		ans = x >= y;
		System.out.println("Ans = "+ ans);	//True

		ans = x == y;
		System.out.println("Ans = "+ ans);	//False

		ans = x != y;
		System.out.println("Ans = "+ ans);	//True


	}
}
